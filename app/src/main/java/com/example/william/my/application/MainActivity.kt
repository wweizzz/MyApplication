class MainActivity : AppCompatActivity() {

    private lateinit var mSplashScreen: SplashScreen

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

        //ViewCompat.setOnApplyWindowInsetsListener(findViewById(android.R.id.content)) { v, insets ->
        //    val systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars())
        //    v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom)
        //    insets
        //}

        if (isRetry()) {
            return
        }

        //判断是否是任务栈中的根Activity，如果是就不做任何处理，如果不是则直接finish掉
        if (!isTaskRoot) {
            finish()
            return
        }

        mSplashScreen = installSplashScreen()
        setContentView(R.layout.activity_main)

        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.S) {
            splashScreen.setOnExitAnimationListener {
                ARouter.getInstance()
                    .build(RouterPath.Module_Main)
                    .withTransition(
                        R.anim.basic_anim_slide_in_left,
                        R.anim.basic_anim_slide_out_right
                    )
                    .withString("param_key", "param_value")
                    .navigation()
                //.greenChannel()//使用绿色通道(跳过所有的拦截器)
                //.navigation()

                finish()
            }
        } else {
            ARouter.getInstance()
                .build(RouterPath.Module_Main)
                .withTransition(
                    R.anim.basic_anim_slide_in_left,
                    R.anim.basic_anim_slide_out_right
                )
                .withString("param_key", "param_value")
                .navigation()
            //.greenChannel()//使用绿色通道(跳过所有的拦截器)
            //.navigation()

            finish()
        }
    }

    /**
     * 避免从桌面启动程序后，会重新实例化入口类的activity
     */
    private fun isRetry(): Boolean {
        if (!isTaskRoot) {
            if (intent.hasCategory(Intent.CATEGORY_LAUNCHER) && Intent.ACTION_MAIN == intent.action) {
                finish()
                return true
            }
        }
        return false
    }
}